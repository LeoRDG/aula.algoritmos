public class Aleatorios {
    public static void main(String[] args) {
        System.out.println( (int) Math.rint( Math.random() * 100 + 100 ) );
        System.out.println( (int) Math.rint( Math.random() * 100 + 100 ) );
        System.out.println( (int) Math.rint( Math.random() * 100 + 100 ) );
        System.out.println( (int) Math.rint( Math.random() * 100 + 100 ) );
        System.out.println( (int) Math.rint( Math.random() * 100 + 100 ) );
    }
}
